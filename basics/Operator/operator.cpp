#include <iostream>
using namespace std;
int main() {
    
    int a;
    cout << "Enter first number: ";
    cin >> a;

    int b;
    cout << "Enter second number: ";
    cin >> b;

    cout << "Addition: " << a + b << "\n";
    cout << "Subtraction: " << a - b << "\n";
    cout << "Multiplication: " << a * b << "\n";
    cout << "Division: " << a / b << "\n";

    return 0;
}